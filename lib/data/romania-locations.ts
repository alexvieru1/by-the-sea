/**
 * Look up cities by county name (case-insensitive, diacritics-sensitive).
 * Returns the sorted city list or an empty array if the county is not found.
 */
export function getCitiesByCounty(countyName: string): string[] {
  const county = counties.find(
    (c) => c.name.toLowerCase() === countyName.toLowerCase()
  );
  return county ? county.cities : [];
}
